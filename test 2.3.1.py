# Escreva
# um algoritmo que leia dois valores e imprima na tela o resultado da
# multiplicação deambos. Entretanto, para calcular a multiplicação, utilize
# somente operações de soma e subtração(Menezes; Nilo, p. 88).

a = int(input('a'))
b = int(input('b'))
j = 0
i = 0
while i < b:
    j += a
    i += 1
print(j)
